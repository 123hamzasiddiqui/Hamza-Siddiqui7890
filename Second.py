import math

base = 10
height = 5
triangle_area = 0.5 * base * height
print("Triangle ka area:", triangle_area)

side = 4
square_area = side * side
print("Square ka area:", square_area)

radius = 7
circle_area = math.pi * radius ** 2
print("Circle ka area:", circle_area)

length = 8
width = 3
rectangle_area = length * width
print("Rectangle ka area:", rectangle_area)
